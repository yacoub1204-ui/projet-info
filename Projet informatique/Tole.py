class Tole:

    def __init__(self, x, y, z, mat):
        self._x = x
        self._y = y
        self._z = z
        self._mat = mat

    def get_x(self):
        return self._x

    def get_y(self):
        return self._y
    
    def get_z(self):
        return self._z

    def get_mat(self):
        return self._mat